    const showLanguageModal = ref(false)

    const username = ref<string>('')
    const avatarUrl = ref<string>('')
    const isBanned = ref(false)
    const defaultAvatar = '/static/avatar/avatar.png'

    const signInDays = ref<number>(0) // 累计签到天数
