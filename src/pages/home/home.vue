        console.error('获取复习单词失败:', error)
        uni.showToast({
          title: '网络错误，请稍后重试',
          icon: 'none',
        })
      }
    }

    // 添加监听词书变化的函数
    watch(() => LexiconStorage.getCurrentLexicon(), (newValue) => {
      currentLexicon.value = newValue
    })

