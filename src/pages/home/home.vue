    const handleLearnClick = async () => {
      if (isBanned.value) {
        uni.showToast({
          title: '哎呦！被封禁啦！',
          icon: 'none',
        })
        return
      }

      const lexicon = LexiconStorage.getCurrentLexicon()
      if (!lexicon) {
        uni.showToast({
          title: '请先选择词书',
          icon: 'none',
        })
        return
      }

      try {
        console.error('词书ID:', lexicon.id)
        const words = await WordAPI.getLearnWords(lexicon.id)
        console.error('获取学习单词:', words.length)
        console.error('单词:', words)
        const token = uni.getStorageSync('token')
        console.error('token:', token)
        if (words && words.length > 0) {
          uni.navigateTo({
            url: `/pages/word/learn?words=${encodeURIComponent(JSON.stringify(words))}`,
          })
        }
        else {
          uni.showToast({
            title: '没有需要学习的单词',
            icon: 'none',
          })
        }
      }
      catch (error) {
        console.error('获取学习单词失败:', error)
        uni.showToast({
          title: '网络错误，请稍后重试',
          icon: 'none',
        })
      }
    }

    const handleReviewClick = async () => {
      if (isBanned.value) {
        uni.showToast({
          title: '哎呦！被封禁啦！',
          icon: 'none',
